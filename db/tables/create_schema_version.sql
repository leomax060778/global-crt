CREATE COLUMN TABLE "MKTG_CART_REQUEST_TOOL".SCHEMA_VERSION
(
	SCHEMA_VERSION_ID BIGINT NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	VERSION NVARCHAR(100) NOT NULL,
	DESCRIPTION NVARCHAR(255),
	SCRIPT NVARCHAR(1000),
	INSTALLED_ON_TZ TIMESTAMP DEFAULT CURRENT_TIMESTAMP     
)

CREATE UNIQUE INDEX idxschversion ON "MKTG_CART_REQUEST_TOOL".SCHEMA_VERSION(VERSION);
