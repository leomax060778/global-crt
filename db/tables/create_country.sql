CREATE COLUMN TABLE "MKTG_CART_REQUEST_TOOL"."COUNTRY"
(	 
	 COUNTRY_ID BIGINT NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	 COUNTRY_NAME_ES NVARCHAR(255),
	 COUNTRY_NAME_EN NVARCHAR(255),
	 COUNTRY_NAME_FR NVARCHAR(255),
	 ISO1 NVARCHAR(255),
	 ISO2 NVARCHAR(255),
	 COUNTRY_CODE INTEGER,
	 CREATED_DATE_TZ TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	 MODIFIED_DATE_TZ TIMESTAMP,
	 CREATED_USER_ID BIGINT NOT NULL ,
	 MODIFIED_USER_ID BIGINT,
	 ENABLED TINYINT DEFAULT 1,
	 DELETED TINYINT DEFAULT 0
) 