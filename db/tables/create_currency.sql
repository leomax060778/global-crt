CREATE COLUMN TABLE "MKTG_CART_REQUEST_TOOL".CURRENCY
(
CURRENCY_ID BIGINT NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
COUNTRY NVARCHAR (255) NOT NULL,
NAME NVARCHAR (255) NOT NULL,
ABBREVIATION NVARCHAR (255) NOT NULL,
CONVERTION_RATE DECIMAL (19,10) NOT NULL,
CURRENCY_YEAR INTEGER NOT NULL,

CREATED_DATE_TZ TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
MODIFIED_DATE_TZ TIMESTAMP,
CREATED_USER_ID BIGINT NOT NULL,
MODIFIED_USER_ID BIGINT,
ENABLED TINYINT DEFAULT 1,
DELETED TINYINT DEFAULT 0
);