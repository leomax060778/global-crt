create column table "MKTG_CART_REQUEST_TOOL".hl2 (
	hl2_id bigint not null primary key generated by default as IDENTITY,
	acronym nvarchar(25) NOT NULL,
	description nvarchar(255) NULL,
	budget_year_id BIGINT NOT NULL,
	region_id BIGINT NULL,
	subregion_id BIGINT NULL,
	created_date_tz timestamp default CURRENT_TIMESTAMP,
	modified_date_tz timestamp default CURRENT_TIMESTAMP,
	created_user_id bigint not null,
	modified_user_id bigint,
	enabled tinyint default 1,
	deleted tinyint default 0,
	team_type_id BIGINT NOT NULL,
	ORGANIZATION_ACRONYM nvarchar(255),
	ORGANIZATION_NAME nvarchar(25),
	HL2_BUDGET_TOTAL decimal(19,2),
	FOREIGN KEY (budget_year_id) REFERENCES "MKTG_CART_REQUEST_TOOL".budget_year(budget_year_id),
	FOREIGN KEY (team_type_id) REFERENCES "MKTG_CART_REQUEST_TOOL".team_type(team_type_id)
);